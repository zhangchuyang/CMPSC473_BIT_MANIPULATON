/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
	//use two's complement to negate the value x
	return (~x + 1);
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
	//change n to the bitwise value first and then
	//shift all 1s to the nth position and flip it to all 0 other than the position needs to be replaced.
	//shift the replace byte to the correct position
	//AND the 0s with x and OR with the shift to get the finalized answer.
	int byte = 0xFF << (n << 3);
	int shift = (c << (n << 3));
	return (~byte & x) | shift;
}
/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
	//change the value of n and m to the bitwise value and then
	//create all 1's form and then according to the n and m to change to the 1 and named combo
	//flip the combo to all 0s other than the specific position and AND with original x
	//return OR with combo cbyte and nbyte to finalized the answer.
	int nByte, mByte, combo, y;
	n = n << 3;
	m = m << 3;
	nByte = x >> n;
	mByte = x >> m;
	combo = ((mByte & 0xFF) << n) | ((nByte & 0xFF) << m);
	y = (0xFF << n) | (0xFF << m);
	return (x & ~y) | combo;
}
/* 
 * isNotEqual - return 0 if x == y, and 1 otherwise 
 *   Examples: isNotEqual(5,5) = 0, isNotEqual(4,5) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNotEqual(int x, int y) {
	//Use XOR x and y
	//if x == y, result will be 0
	//if x != y, result will be 1
	//! will make the result be either 0 or 1
	return !!(x ^ y);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
	//change x to either 0 or 1
	//negate x
	//if x == 0, return z
	//if x == 1, return y
	x = !(!x);
	x = ~x + 1;  
	return (x & y) | (~x & z);
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
	//OR the value x with half of the x each time
	//accumulate the result and
	//flip the x and AND with the 0x1 to finalized the result
	x = (x >> 16) | x;
	x = (x >> 8) | x;
	x = (x >> 4) | x;
	x = (x >> 2) | x;
	x = (x >> 1) | x;
	return ~x & 0x1;
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
	//shift to get the sign of x and y
	//if the sign is the same, sign bit of (~y + x) will be different depends on whether x is larger or smaller than y
	//if the sign is different, the rules will be exact opposite
	//OR return 0 if x is greater, 1 if x is smaller
	//negate the result to finalized the answer.
	int sign_x = x >> 31;
	int sign_y = y >> 31;
	int equal = (!(sign_x ^ sign_y)) & ((~y + x) >> 31);
	int notEqual = sign_x & !sign_y;  
	return !(equal | notEqual);
}
/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
	//shift x to get the sign bit of x and allocated to a
	//XOR a with x and plus the two's complement of a to get the absolute value of x 		
	int a = x >> 31;
	return (a ^ x) + (1 + ~a);
}
/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
	//sum the x and y and allocated to sum
	//get the sign of x, y and sum
	//check whether the sum's sign bit is the same as sign bit of x and y to decide whether it's overflowed or not.
	int sum = x + y;
	int sign_x = x >> 31;
	int sign_y = y >> 31;
	int sign_sum = sum >> 31;  
	return !(~(sign_x ^ sign_y) & (sign_x ^ sign_sum));
}
/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) {
	//each time x will be split into half and the result will have the same parity as the original code.
	x = x ^ (x >> 16);
	x = x ^ (x >> 8);
	x = x ^ (x >> 4);
	x = x ^ (x >> 2);
	x = x ^ (x >> 1);
	return x & 0x01;		
}
